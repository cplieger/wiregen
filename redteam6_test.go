// Test 5: Option order does NOT matter - applying in different order gives same result
func TestR6_OptionOrderIndependence(t *testing.T) {
	opts1 := []wiregen.Option{
		wiregen.WithValidatorsImport("./v.js"),
		wiregen.WithBusImport("./b.js"),
		wiregen.WithTypesImportPath("./custom.js"),
		wiregen.WithHeaderComment("// H\n\n"),
		wiregen.WithRegisterFuncName("regFn"),
		wiregen.WithRegistryFuncName("allFn"),
		wiregen.WithSelfContainedRegistry(false),
	}
	opts2 := []wiregen.Option{
		wiregen.WithSelfContainedRegistry(false),
		wiregen.WithRegistryFuncName("allFn"),
		wiregen.WithRegisterFuncName("regFn"),
		wiregen.WithHeaderComment("// H\n\n"),
		wiregen.WithTypesImportPath("./custom.js"),
		wiregen.WithBusImport("./b.js"),
		wiregen.WithValidatorsImport("./v.js"),
	}

	setup := func(r *wiregen.Registry) {
		r.WireTypes = []reflect.Type{reflect.TypeFor[User](), reflect.TypeFor[Address]()}
		r.Enums = map[string]wiregen.EnumDef{"Status": {Values: []string{"active"}}}
		r.SSEEvents = []wiregen.SSERegEntry{{EventType: "u", TypeName: "User"}}
	}

	r1 := wiregen.NewRegistry(opts1...)
	setup(r1)
	r2 := wiregen.NewRegistry(opts2...)
	setup(r2)

	if r1.GenerateTypes() != r2.GenerateTypes() {
		t.Error("option order affected GenerateTypes")
	}
	if r1.GenerateDecoders() != r2.GenerateDecoders() {
		t.Error("option order affected GenerateDecoders")
	}
	if r1.GenerateRegistry() != r2.GenerateRegistry() {
		t.Error("option order affected GenerateRegistry")
	}
}

