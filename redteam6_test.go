// Test 12: Determinism - 300 iterations with options-created registry
func TestR6_DeterminismWithOptions(t *testing.T) {
	makeReg := func() *wiregen.Registry {
		r := wiregen.NewRegistry(
			wiregen.WithValidatorsImport("./v.js"),
			wiregen.WithBusImport("./b.js"),
			wiregen.WithHeaderComment("// H\n\n"),
			wiregen.WithTypesImportPath("./t.js"),
			wiregen.WithRegisterFuncName("reg"),
			wiregen.WithRegistryFuncName("all"),
		)
		r.WireTypes = []reflect.Type{
			reflect.TypeFor[Address](),
			reflect.TypeFor[User](),
			reflect.TypeFor[Notification](),
		}
		r.Enums = map[string]wiregen.EnumDef{
			"Status": {Values: []string{"active", "inactive"}},
		}
		r.SSEEvents = []wiregen.SSERegEntry{
			{EventType: "notif", TypeName: "Notification"},
			{EventType: "user", TypeName: "User"},
		}
		return r
	}

	ref := makeReg()
	typesRef := ref.GenerateTypes()
	decRef := ref.GenerateDecoders()
	regRef := ref.GenerateRegistry()

	for i := range 300 {
		r := makeReg()
		if r.GenerateTypes() != typesRef {
			t.Fatalf("iter %d: GenerateTypes differs", i)
		}
		if r.GenerateDecoders() != decRef {
			t.Fatalf("iter %d: GenerateDecoders differs", i)
		}
		if r.GenerateRegistry() != regRef {
			t.Fatalf("iter %d: GenerateRegistry differs", i)
		}
	}
}

// Test 13: Embedded ambiguity omission (regression)
func TestR6_EmbedAmbiguityRegression(t *testing.T) {
	type EA struct {
		F string `json:"f"`
	}
	type EB struct {
		F int `json:"f"`
	}
	host := reflect.StructOf([]reflect.StructField{
		{Name: "EA", Type: reflect.TypeFor[EA](), Anonymous: true},
		{Name: "EB", Type: reflect.TypeFor[EB](), Anonymous: true},
		{Name: "G", Type: reflect.TypeFor[string](), Tag: `json:"g"`},
	})
	r := &wiregen.Registry{
		WireTypes:        []reflect.Type{host},
		ValidatorsImport: "./v.js",
		BusImport:        "./b.js",
	}
	out := r.GenerateTypes()
	if strings.Contains(out, "f:") || strings.Contains(out, "f?:") {
		t.Errorf("ambiguous field f should be omitted, got:\n%s", out)
	}
	if !strings.Contains(out, "g: string;") {
		t.Errorf("non-ambiguous field g missing, got:\n%s", out)
	}
}

