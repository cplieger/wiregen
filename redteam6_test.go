// Test 1: NewRegistry() with no options produces IDENTICAL output as &Registry{}
// for the same payload. This is the core back-compat guarantee.
func TestR6_DefaultParity_NoOptions(t *testing.T) {
	payload := func(r *wiregen.Registry) {
		r.WireTypes = []reflect.Type{
			reflect.TypeFor[Address](),
			reflect.TypeFor[User](),
			reflect.TypeFor[Notification](),
		}
		r.Enums = map[string]wiregen.EnumDef{
			"Status": {Values: []string{"active", "inactive", "banned"}},
		}
		r.SSEEvents = []wiregen.SSERegEntry{
			{EventType: "notification", TypeName: "Notification"},
		}
		r.ValidatorsImport = "./v.js"
		r.BusImport = "./b.js"
	}

	// Zero-value registry
	zv := &wiregen.Registry{}
	payload(zv)

	// NewRegistry with no options
	nr := wiregen.NewRegistry()
	payload(nr)

	zvTypes := zv.GenerateTypes()
	nrTypes := nr.GenerateTypes()
	if zvTypes != nrTypes {
		t.Errorf("GenerateTypes differs between &Registry{} and NewRegistry():\n--- zero-value ---\n%s\n--- NewRegistry ---\n%s", zvTypes, nrTypes)
	}

	zvDec := zv.GenerateDecoders()
	nrDec := nr.GenerateDecoders()
	if zvDec != nrDec {
		t.Errorf("GenerateDecoders differs between &Registry{} and NewRegistry():\n--- zero-value ---\n%s\n--- NewRegistry ---\n%s", zvDec, nrDec)
	}

	zvReg := zv.GenerateRegistry()
	nrReg := nr.GenerateRegistry()
	if zvReg != nrReg {
		t.Errorf("GenerateRegistry differs between &Registry{} and NewRegistry():\n--- zero-value ---\n%s\n--- NewRegistry ---\n%s", zvReg, nrReg)
	}
}

// Test 2: Verify init() defaults match documentation expectations
func TestR6_InitDefaults(t *testing.T) {
	r := wiregen.NewRegistry()
	r.WireTypes = []reflect.Type{reflect.TypeFor[Address]()}
	r.ValidatorsImport = "./v.js"
	r.BusImport = "./b.js"
	r.SSEEvents = []wiregen.SSERegEntry{{EventType: "addr", TypeName: "Address"}}

	types := r.GenerateTypes()
	if !strings.HasPrefix(types, "// CODE-GENERATED by wiregen, DO NOT EDIT.\n\n") {
		t.Errorf("default HeaderComment not applied, got prefix: %q", types[:min(80, len(types))])
	}

	dec := r.GenerateDecoders()
	if !strings.Contains(dec, `from "./types.gen.js"`) {
		t.Errorf("default TypesImportPath not applied, got:\n%s", dec)
	}

	reg := r.GenerateRegistry()
	if !strings.Contains(reg, "registerSSEDecoder") {
		t.Errorf("default RegisterFuncName not applied, got:\n%s", reg)
	}
	if !strings.Contains(reg, "export function registerAllSSEDecoders()") {
		t.Errorf("default RegistryFuncName not applied, got:\n%s", reg)
	}
}

