func TestPanicOnEmptyValidatorsImport(t *testing.T) {
	r := &wiregen.Registry{
		WireTypes: []reflect.Type{
			reflect.TypeFor[Notification](),
		},
		ValidatorsImport: "", // must panic
	}
	defer func() {
		rec := recover()
		if rec == nil {
			t.Fatal("expected panic when ValidatorsImport is empty, but did not panic")
		}
		msg, ok := rec.(string)
		if !ok || !strings.Contains(msg, "ValidatorsImport must be set") {
			t.Fatalf("unexpected panic message: %v", rec)
		}
	}()
	r.GenerateDecoders()
}

