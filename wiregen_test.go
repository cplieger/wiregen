func TestGenerateRegistry_SelfContained(t *testing.T) {
	r := wiregen.NewRegistry(
		wiregen.WithValidatorsImport("./test-validators.js"),
		wiregen.WithSelfContainedRegistry(true),
	)
	r.WireTypes = []reflect.Type{
		reflect.TypeFor[Notification](),
	}
	r.SSEEvents = []wiregen.SSERegEntry{
		{EventType: "notification", TypeName: "Notification"},
	}
	out := r.GenerateRegistry()

	if !strings.Contains(out, "new Map<string, Decoder<unknown>>()") {
		t.Errorf("missing Map declaration, got:\n%s", out)
	}
	if !strings.Contains(out, "registry.set(") {
		t.Errorf("missing registry.set call, got:\n%s", out)
	}
	if !strings.Contains(out, "getSSEDecoder") {
		t.Errorf("missing getSSEDecoder function, got:\n%s", out)
	}
	if strings.Contains(out, "import { registerSSEDecoder }") {
		t.Errorf("should NOT contain registerSSEDecoder import in self-contained mode, got:\n%s", out)
	}
}

